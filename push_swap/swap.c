#include "push_swap.h"

void	sa(int **a)
{
	int	tmp;

	tmp = (*a)[0];
	(*a)[0] = (*a)[1];
	(*a)[1] = tmp;
}

void	sb(int **b)
{
	int	tmp;

	tmp = (*b)[0];
	(*b)[0] = (*b)[1];
	(*b)[1] = tmp;
}

void	ss(int **a, int **b)
{
	sa(a);
	sb(b);
}
